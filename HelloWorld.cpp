#include <iostream> //this is a header 
using namespace std;  // here we are saying we want to use the namespace which is named as std
int main(){  // this is our function , this is used in almost all programs
    cout<<"hello World"; // here we are asking to output hello world by using cout object with the insertion operation<<
    return 0;  // this return keyword closes the our main function and returns the code zero when we execute it 
    
}

